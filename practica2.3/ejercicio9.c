/*
NAME
       kill - terminate a process

SYNOPSIS
       kill [-s signal|-p] [-q sigval] [-a] [--] pid...
       kill -l [signal]

DESCRIPTION
       The command kill sends the specified signal to the specified process or process group. 

       -p, --pid
              Specify that kill should only print the process id (pid) of the named  processes,  and
              not send any signals.
*/